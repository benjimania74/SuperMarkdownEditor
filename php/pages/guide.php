<div id="rules">
    <div id="title">
        <p>En Markdown les titres fonctione avec des #, on peut en mettre jusqu'à 6, et plus il y en a plus le titre est petit</p>
        <p>Exemple :</p>
        <code> 
            # Titre 1 = <h1>Titre 1</h1>
            ## Titre 2 = <h2>Titre 2</h2>
            ### Titre 3 = <h3>Titre 3</h3>
            #### Titre 4 = <h4>Titre 4</h4>
            ##### Titre 5 = <h5>Titre 5</h5>
            ###### Titre 6 = <h6>Titre 6</h6>
    </div>
    <div id="bold">
        <p>Pour mettre du texte en gras, il faut le mettre entre deux **</p>
        <p>Exemple :</p>
        <code>**Texte en gras** = <strong>Texte en gras</strong></code>
    </div>
    <div id="italic">
        <p>Pour mettre du texte en italique, il faut le mettre entre deux * ou _</p>
        <p>Exemple :</p>
        <code>*Texte en italique* = <em>Texte en italique</em></code>
    </div>
    <div id="underline">
        <p>Pour souligner du texte, il faut le mettre entre deux __</p>
        <p>Exemple :</p>
        <code>__Texte souligné__ = <u>Texte souligné</u></code>
    </div>
    <div id="strikethrough">
        <p>Pour barrer du texte, il faut le mettre entre deux ~~</p>
        <p>Exemple :</p>
        <code>~~Texte barré~~ = <del>Texte barré</del></code>
    </div>
    <div id="link">
        <p>Pour mettre un lien, il faut le mettre entre deux [] et ()</p>
        <p>Exemple :</p>
        <code>[Texte du lien](https://www.google.com) = <a href="https://www.google.com">Texte du lien</a></code>
    </div>
    <div id="image">
        <p>Pour mettre une image, il faut le mettre entre deux ![] et ()</p>
        <p>Exemple :</p>
        <code>![Texte de l'image](https://www.google.com/image.png) = <img src="https://www.google.com/image.png" alt="Texte de l'image"></code>
    </div>
    <div id="list">
        <p>Pour faire une liste, il faut mettre un * ou - devant chaque élément</p>
        <p>Exemple :</p>
        <code>* Élément 1<br>- Élément 2<br>* Élément 3 = <ul><li>Élément 1</li><li>Élément 2</li><li>Élément 3</li></ul></code>
    </div>
    <div id="numbered-list">
        <p>Pour faire une liste numérotée, il faut mettre un 1. devant chaque élément</p>
        <p>Exemple :</p>
        <code>1. Élément 1<br>2. Élément 2<br>3. Élément 3 = <ol><li>Élément 1</li><li>Élément 2</li><li>Élément 3</li></ol></code>
    </div>
    <div id="quote">
        <p>Pour faire une citation, il faut mettre un > devant chaque élément</p>
        <p>Exemple :</p>
        <code>> Élément 1<br>> Élément 2<br>> Élément 3 = <blockquote><p>Élément 1</p><p>Élément 2</p><p>Élément 3</p></blockquote></code>
    </div>
    <div id="code">
        <p>Pour faire un code, il faut le mettre entre deux ``</p>
        <p>Exemple :</p>
        <code>`Code` = <code>Code</code></code>
    </div>
    <div id="code-block">
        <p>Pour faire un bloc de code, il faut le mettre entre trois ``</p>
        <p>Exemple :</p>
        <code>``Code`` = <pre><code>Code</code></pre></code>
    </div>
    <div id="horizontal-rule">
        <p>Pour faire une ligne horizontale, il faut mettre trois ---</p>
        <p>Exemple :</p>
        <code>--- = <hr></code>
    </div>
    <div id="table">
        <p>Pour faire un tableau, il faut mettre | devant chaque élément</p>
        <p>Exemple :</p>
        <code>| Élément 1 | Élément 2 | Élément 3 |<br>| Élément 4 | Élément 5 | Élément 6 | = <table><tr><td>Élément 1</td><td>Élément 2</td><td>Élément 3</td></tr><tr><td>Élément 4</td><td>Élément 5</td><td>Élément 6</td></tr></table></code>
    </div>
    <div id="footnote">
        <p>Pour faire une note de bas de page, il faut mettre [^1] devant chaque élément</p>
        <p>Exemple :</p>
        <code>[^1] Élément 1<br>[^2] Élément 2<br>[^3] Élément 3 = <sup>1</sup>Élément 1<br><sup>2</sup>Élément 2<br><sup>3</sup>Élément 3</code>
    </div>
    <div id="highlight">
        <p>Pour faire un surlignage, il faut mettre == devant et derrière le texte</p>
        <p>Exemple :</p>
        <code>==Texte surligné== = <mark>Texte surligné</mark></code>
    </div>
    <div id="emoji">
        <p>Pour faire un emoji, il faut mettre : devant et derrière le texte</p>
        <p>Exemple :</p>
        <code>:smile: = <img src="https://www.google.com/smile.png" alt=":smile:"></code>
    </div>
    <div id="math">
        <p>Pour faire une équation, il faut mettre $ devant et derrière le texte</p>
        <p>Exemple :</p>
        <code>$E=mc^2$ = <img src="https://www.google.com/equation.png" alt="$E=mc^2$"></code>
    </div>
    
</div>